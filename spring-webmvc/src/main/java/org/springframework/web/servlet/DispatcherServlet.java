	/**
	 * 初始化策略属性对象（与spring mvc的配置文件息息相关）
	 */
	protected void initStrategies(ApplicationContext context) {
		initMultipartResolver(context);
		// 初始化multipartResolver

		// 初始化本地化resolver
		initLocaleResolver(context);

		// 初始化主题resolver
		initThemeResolver(context);

		// 初始化配置的HandlerMapping，若没有配置则默认使用BeanNameUrlHandlerMapping
		initHandlerMappings(context);

		// 初始化HandlerAdapters，若没配置则默认使用SimpleControllerHandlerAdapter.
		initHandlerAdapters(context);

		// 初始化配置的HandlerExceptionResolver，若没配置则默认为空
		initHandlerExceptionResolvers(context);

		// 初始化配置的RequestToViewNameTranslator，若无配置，则默认使用DefaultRequestToViewNameTranslator.
		initRequestToViewNameTranslator(context);

		// 初始化配置的ViewResolvers，若无配置则默认使用InternalResourceViewResolver.
		initViewResolvers(context);

		// 初始化配置的FlashMapManager实现实例，若未配置，则默认使用org.springframework.web.servlet.support.DefaultFlashMapManager
		initFlashMapManager(context);
	}

	/**
	 * 初始化文件上传resolver
	 * springmvc 的配置文件中若没有配置name是multipartResolver的resolver，则认为没有处理文件上传的类
	 */
	private void initMultipartResolver(ApplicationContext context) {
		try {
			this.multipartResolver = context.getBean(MULTIPART_RESOLVER_BEAN_NAME, MultipartResolver.class);
			if (logger.isDebugEnabled()) {
				logger.debug("Using MultipartResolver [" + this.multipartResolver + "]");
			}
		} catch (NoSuchBeanDefinitionException ex) {
			this.multipartResolver = null;
			if (logger.isDebugEnabled()) {
				logger.debug("Unable to locate MultipartResolver with name '" + MULTIPART_RESOLVER_BEAN_NAME +
						"': no multipart request handling provided");
			}
		}
	}

